package com.diamondedge.chart

enum class StrokeStyle {
    /**
     * A solid line.
     */
    Solid,

    /**
     * A dashed line.
     */
    Dash,

    /**
     * A dotted line
     */
    Dot,

    /**
     * Alternating dashes and dotted lines.
     */
    DashDot,

    /**
     * Alternating dashes and two dotted lines.
     */
    DashDotDot
}